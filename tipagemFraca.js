/**
 * Javascript é uma linguagem dinamicamente tipada (fracamente tipada)
 * Não é preciso definir o tipo da linguagem
 */

let qualquer = 'Legal'
console.log(qualquer) //printa na tela o que está armazenado na variável "qualquer"
console.log(typeof qualquer) //printa na tela o tipo da variável "qualquer"

/* Outro exemplo */
qualquer = 3
console.log(typeof qualquer)
